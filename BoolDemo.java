class BoolDemo {
    public static void main(String args[]) {
        boolean flag1, flag2, flag3;
        
        // Assingning values to boolean type variables.
        flag1 = true;
        flag2 = false;
        flag3 = true;

        // Check the value of flag1
        if (flag1) System.out.println("Flag1 is true.");
        else System.out.println("Flag1 is false.");
        
        // Check the value of flag2
        if (flag2) System.out.println("Flag2 is true.");
        else System.out.println("Flag2 is false.");
        
        // Check the value of falg3
        if (flag3) System.out.println ("Flag3 is true.");
        else System.out.println("Flag3 is false.");
    }
}